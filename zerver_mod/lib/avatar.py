import base64
from io import BytesIO

from PIL import Image, ImageDraw, ImageFont

from zerver.lib.upload import MEDIUM_AVATAR_SIZE


def gen_avatar(full_name: str, letters: str) -> BytesIO:
    assert len(letters) == 2
    top_color, bottom_color, text_color = _text_to_colors(full_name)
    image = Image.new("RGBA", (1, 2))
    draw = ImageDraw.Draw(image)
    draw.point((0, 0), fill=top_color)
    draw.point((0, 1), fill=bottom_color)
    image = image.resize((MEDIUM_AVATAR_SIZE, MEDIUM_AVATAR_SIZE))
    draw = ImageDraw.Draw(image)
    center = round(MEDIUM_AVATAR_SIZE / 2)
    draw.text((center, center), letters, fill=text_color, font=_get_font(), anchor='mm')
    result = BytesIO()
    image.save(result, format="png")
    result.seek(0)
    return result


def _text_to_colors(s):
    hash_val = 0
    for c in s:
        hash_val = ord(c) + ((hash_val << 5) - hash_val)
    r = hash_val & 0xff
    g = (hash_val >> 8) & 0xff
    b = (hash_val >> 16) & 0xff
    text_color = 'black' if (r*0.299 + g*0.587 + b*0.114) > 186 else 'white'
    k = 0.1
    k1, k2 = 1+k, 1-k
    r1, g1, b1 = min(255, round(r*k1)), min(255, round(g*k1)), min(255, round(b*k1))
    r2, g2, b2 = round(r*k2), round(g*k2), round(b*k2)
    return (r1, g1, b1), (r2, g2, b2), text_color


